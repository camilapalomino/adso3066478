let a = 8;
let b = 2;

// Multiplicación
//typeof para obtener el tipo de una variable//
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: * ');
console.log('La segunda variable es: ' + b);
let c = a * b;
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

// División
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: / ');
console.log('La segunda variable es: ' + b);
c = a / b;
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

// Suma
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: + ');
console.log('La segunda variable es: ' + b);
c = a + b;
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

// Resta
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: - ');
console.log('La segunda variable es: ' + b);
c = a - b;
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

// Exponente (potencia)
//Math.pow(a, b) para calcular la potencia//
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: ** ');
console.log('La segunda variable es: ' + b);
c = Math.pow(a, b);  // Usamos Math.pow() para exponente en JavaScript
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

// Módulo (resto de la división)
//% en JavaScript para obtener el resto de la división.//
console.log('La primera variable es: ' + a);
console.log('El signo de la operación es: % ');
console.log('La segunda variable es: ' + b);
c = a % b;  
console.log('El resultado es: ' + c);
console.log('Tipo de la variable: ' + typeof c);

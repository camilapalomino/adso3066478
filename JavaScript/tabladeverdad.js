// Variables booleanas
let a = true;
let b = false;

// && equivale al operador lógico AND
console.log((a && b) ? 'true' : 'false');

a = 2;
b = 3;
c = 4;
d = 10;

// Comprobación de igualdad y comparación
console.log(((a == b) && (c < d)) ? 'true' : 'false');

// ! significa NOT
console.log((!(a == b) && (c > d)) ? 'true' : 'false');
